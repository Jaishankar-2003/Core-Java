public class cls_nestedif
{
    public static void main (String[] args)
    {
        char input = 'A';
        if(input == 'A')
        {
            System.out.println("Apple");
        }
        else if (input == 'B')
        {
            System.out.println("Ball");
        }
        else if (input == 'C')
        {
            System.out.println("cat");
        }
        else if (input == 'D')
        {
            System.out.println("Dog");
        }
        else
        {
            System.out.println("invalid char input");
        }
    }
}
