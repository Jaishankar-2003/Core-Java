
// display the  count of all even num between 101 to 201

public class cls_example_42 {
    public static void main (String[] args)
    {
        int i = 55;
        int count = 0;
        while(i<=99)
        {
            if(i%2==0)
            {
                count++;

            }
            i++;
        }

        System.out.println(count);
    }
}
